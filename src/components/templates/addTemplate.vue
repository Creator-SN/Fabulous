<template>
    <float-window-base
        v-model="thisShow"
        :title="local('Add Template')"
        :theme="theme"
    >
        <template v-slot:content>
            <div class="w-p-block">
                <p class="w-title">{{local('Template Name')}}</p>
                <fv-text-box
                    v-model="name"
                    :placeholder="local('Input template name...')"
                    :theme="theme"
                    :font-size="18"
                    :font-weight="'bold'"
                    underline
                    :focus-border-color="'rgba(123, 139, 209, 1)'"
                    :is-box-shadow="true"
                    style="width: 100%; height: 60px; margin-top: 15px;"
                    @keyup.enter="add"
                ></fv-text-box>
            </div>
        </template>
        <template v-slot:control>
            <fv-button
                theme="dark"
                background="rgba(0, 98, 158, 1)"
                :disabled="name === '' || !ds_db"
                @click="add"
            >{{local('Confirm')}}</fv-button>
            <fv-button
                :theme="theme"
                style="margin-left: 5px;"
                @click="thisShow = false"
            >{{local('Cancel')}}</fv-button>
        </template>
    </float-window-base>
</template>

<script>
import floatWindowBase from "../window/floatWindowBase.vue";
import { page } from "@/js/data_sample.js";
import { mapMutations, mapState, mapGetters } from "vuex";
const { ipcRenderer: ipc } = require("electron");
const path = require("path");

export default {
    components: {
        floatWindowBase,
    },
    props: {
        show: {
            default: false,
        },
    },
    data() {
        return {
            thisShow: this.show,
            name: "",
        };
    },
    watch: {
        show(val) {
            this.thisShow = val;
        },
        thisShow(val) {
            this.$emit("update:show", val);
            this.name = "";
        },
    },
    computed: {
        ...mapState({
            data_index: (state) => state.config.data_index,
            data_path: (state) => state.config.data_path,
            templates: (state) => state.data_structure.templates,
            theme: (state) => state.config.theme,
        }),
        ...mapGetters(["local", "ds_db"]),
        v() {
            return this;
        },
    },
    mounted() {
        this.eventInit();
    },
    methods: {
        ...mapMutations({
            reviseData: "reviseData",
        }),
        eventInit() {
            ipc.on("output-file-callback", () => {
                this.thisShow = false;
            });
        },
        async add() {
            if (!this.ds_db || this.name === "") return;
            let _page = JSON.parse(JSON.stringify(page));
            _page.id = this.$Guid();
            _page.name = this.name;
            _page.emoji = "ðŸ“‘";
            _page.createDate = this.$SDate.DateToString(new Date());
            this.templates.push(_page);
            this.reviseData({
                templates: this.templates,
            });
            let url = path.join(
                this.data_path[this.data_index],
                "root/templates",
                `${_page.id}.json`
            );
            ipc.send("output-file", {
                path: url,
                data: "",
            });
        },
    },
};
</script>

